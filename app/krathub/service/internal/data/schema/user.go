package schema

import (
	"time"

	"entgo.io/ent"
	"entgo.io/ent/dialect/entsql"
	"entgo.io/ent/schema"
	"entgo.io/ent/schema/field"
)

type User struct {
	ent.Schema
}

func (User) Fields() []ent.Field {
	return []ent.Field{
		field.Int64("id"),
		field.String("name").MaxLen(64).Unique(),
		field.String("email").MaxLen(128).Unique(),
		field.String("password").MaxLen(255),
		field.String("phone").MaxLen(20).Optional().Nillable(),
		field.String("avatar").MaxLen(255).Optional().Nillable(),
		field.String("bio").MaxLen(255).Optional().Nillable(),
		field.String("location").MaxLen(128).Optional().Nillable(),
		field.String("website").MaxLen(255).Optional().Nillable(),
		field.String("role").MaxLen(32).Default("user"),
		field.Time("created_at").Default(time.Now).Immutable(),
		field.Time("updated_at").Default(time.Now).UpdateDefault(time.Now),
	}
}

func (User) Edges() []ent.Edge {
	return nil
}

func (User) Annotations() []schema.Annotation {
	return []schema.Annotation{
		entsql.Annotation{Table: "users"},
	}
}
